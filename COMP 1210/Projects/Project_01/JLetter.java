/**
 * Simple program to print the word 'JAVA' many times
 * that forms a capital 'J'.
 *
 * Project 1
 * @author Midas Oden
 * @version August 26, 2019
 */
 
public class JLetter
{
    /**
     * Prints the word 'JAVA' to form the letter 'J'.
     * @param args Command line arguments – not used.
     */
   public static void main(String[] args) 
   {
   
      System.out.println("JAVAJAVAJAVA");
      System.out.println("JAVAJAVAJAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("J     JAVA");
      System.out.println("JA    JAVA");
      System.out.println(" JAVAJAVA");
      System.out.println("  JAVAJA");
      
   }

}